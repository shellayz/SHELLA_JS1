let hobby = "Tidur";

switch (hobby) {
  case "Membaca":
    console.log("Hobby Anda adalah Membaca.");
    break;
  case "Menonton":
    console.log("Hobby Anda adalah Menonton.");
    break;
  case "Melukis":
    console.log("Hobby Anda adalah Melukis.");
    break;
  case "Menulis":
    console.log("Hobby Anda adalah Menulis.");
    break;
  default:
    console.log(`Maaf ${hobby} bukan termasuk hobby.`);
}